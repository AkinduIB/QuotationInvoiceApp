    private void setCurrentDate() {
     Date date = new Date();
     SimpleDateFormat dateFormat = new SimpleDateFormat("dd");
     String day = dateFormat.format(date);
     String daySuffix = getDaySuffix(Integer.parseInt(day));

     dateFormat = new SimpleDateFormat("dd'" + daySuffix + "' MMMM yyyy");
     String formattedDate = dateFormat.format(date);

     jLabel7.setText(formattedDate);
    }

    private String getDaySuffix(int day) {
        if (day >= 11 && day <= 13) {
            return "th";
        }
        switch (day % 10) {
            case 1:  return "st";
            case 2:  return "nd";
            case 3:  return "rd";
            default: return "th";
        }
    }
    
    private void loadCompanyFileContent(String companyName) {
        File quotationFolder = new File("Quotation");
        File file = new File(quotationFolder, companyName + ".txt");

        if (file.exists()) {
            try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
                StringBuilder content = new StringBuilder();
                String line;
                while ((line = reader.readLine()) != null) {
                    content.append(line).append("\n");
                }
                jTextArea3.setText(content.toString());
            } catch (IOException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "An error occurred while loading the file.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            jTextArea3.setText(""); // Clear the text area if the file does not exist
        }
    }
    
    



    
