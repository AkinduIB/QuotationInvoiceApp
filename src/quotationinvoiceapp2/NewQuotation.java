    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String client = jTextArea1.getText().replace("\n", " ");
        String description = txtDescription.getText();
        String amount = txtAmount.getText();
        String companyName = jTextField1.getText().trim();

        if (companyName.isEmpty()) {
            // Handle the case when company name is not entered
            JOptionPane.showMessageDialog(this, "Please enter a company name.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        File quotationFolder = new File("Quotation");
        if (!quotationFolder.exists()) {
            quotationFolder.mkdir();
        }

        // Create the file in the Quotation folder
        File file = new File(quotationFolder, companyName + ".txt");
        
        if (file.exists()){
            JOptionPane.showMessageDialog(this, "File name already exists. Please type a different file name.","Error",JOptionPane.ERROR_MESSAGE);
            return;        
        }
        
        // Prepare the data to be saved in the file
        String data = description + "\t\t\t" + amount + "\n";

        try (BufferedWriter writer = new BufferedWriter(new FileWriter(file, true))) {
            // Append the data to the file
            writer.write(data);
            writer.newLine();
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred while saving the data.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        // Append the data to the JTextArea for display
        jTextArea3.append(data);
        jTextArea3.append("\n\n");

        // Clear the input fields for new data
        txtDescription.setText("Enter Description");
        txtDescription.setForeground(new Color(153, 153, 153));
        txtAmount.setText("Enter Amount (Rs.)");
        txtAmount.setForeground(new Color(153, 153, 153));
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String companyName = jTextField1.getText().trim();

        if (companyName.isEmpty()) {
            // Handle the case when company name is not entered
            JOptionPane.showMessageDialog(this, "Please enter a company name.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        File quotationFolder = new File("Quotation");
        if (!quotationFolder.exists()) {
            quotationFolder.mkdir();
        }

        // Create the file in the Quotation folder
        File file = new File(quotationFolder, companyName + ".txt");

        if (!file.exists()) {
            JOptionPane.showMessageDialog(this, "The file does not exist. Cannot edit non-existing file.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Get the edited content from jTextArea3
        String editedContent = jTextArea3.getText();

        // Write the edited content back to the file
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
            writer.write(editedContent);
            writer.newLine();
            JOptionPane.showMessageDialog(this, "File updated successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred while saving the data.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

